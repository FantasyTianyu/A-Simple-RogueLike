
public class RectInfo {

    public int posX;
    public int posY;
    public int width;
    public int height;

    public RectInfo(int posX,int posY,int width,int height)
    {
        this.posX = posX;
        this.posY = posY;
        this.width = width;
        this.height = height;
    }
}
